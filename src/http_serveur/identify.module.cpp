void IDENTIFY_MODULE(AsyncWebServerRequest *request)
{
    diybms_eeprom_settings *_mysettings = SETTINGS_Get();
    if (request->hasParam("c", false) && request->hasParam("cmi", false))
    {
        AsyncWebParameter *ctrlId = request->getParam("c", false);
        uint8_t _ctrl = ctrlId->value().toInt();

        AsyncWebParameter *cellId = request->getParam("cmi", false);
        uint8_t _cmi = cellId->value().toInt();

        if ((_ctrl > _mysettings->totalControllers) || (_cmi > (_mysettings->totalNumberOfBanks[_ctrl] * _mysettings->totalNumberOfSeriesModules[_ctrl])))
        {
            request->send(500, "text/plain", "Wrong parameter bank");
            return;
        }
        else
        {
            MODBUS_SendIdentifyModule(_ctrl,_cmi);
            AsyncResponseStream *response = request->beginResponseStream("application/json");
            StaticJsonDocument<100> doc;
            doc["success"] = true;
            serializeJson(doc, *response);
            request->send(response);
        }
    }
    else
    {
        request->send(500, "text/plain", "Missing parameters");
    }
}