// DESCRIPTION : Initialisation de la carte : GPIO, Clocks, Interruptions...
//--------------------------------------------------------------------------------------------------
void MEM_TaskInit(void)
{
    EEPROM.begin(1024);
}

void MEM_SetSSID(String str){
    EEPROM.writeString(0, str.c_str());
    EEPROM.commit();
}

String MEM_GetSSID(void){
    return EEPROM.readString(0);
}

void MEM_SetPSK(String str){
    EEPROM.writeString(32, str.c_str());
    EEPROM.commit();
}

String MEM_GetPSK(void){
    return EEPROM.readString(32);
}
