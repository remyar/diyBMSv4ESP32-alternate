#include "Event.h"
#include "Task.h"

//=============================================================================
//--- DEFINITIONS
//=============================================================================
extern s_TASK tasks[MAX_TASKS];

//-----------------------------------------------------------------------------
// FONCTION    : EVENT_None
//
// DESCRIPTION :
//-----------------------------------------------------------------------------
void EVENT_None(uint8_t idx)
{
    tasks[idx].event.type = NO_EVENT;
}

//-----------------------------------------------------------------------------
// FONCTION    : EVENT_Clear
//
// DESCRIPTION :
//-----------------------------------------------------------------------------
void EVENT_Clear(uint8_t idx, uint16_t type)
{
    tasks[idx].event.type &= ~type;
}